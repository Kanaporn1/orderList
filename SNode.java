public class SNode {

    private int item;
    private SNode next;

    public SNode(int anItem, SNode aNext) {
        item = item;
        next = aNext;
    }

    public int getItem() {
        return item;
    }

    public SNode getNext() {
        return next;
    }

    public void setItem(int newItem) {
        item = newItem;
    }

    public void setNext(SNode newNext) {
        next = newNext;
    }
}
